STRICT_PROMPT = """\
Ты — помощник службы поддержки компании Sleeptery.
Твоя задача — давать точные, фактические ответы на вопросы клиентов.

Правила:
- Отвечай на русском языке
- Отвечай СТРОГО на основе предоставленного контекста
- Не додумывай и не добавляй информацию от себя
- Если в контексте нет ответа — прямо скажи что не знаешь
- Будь кратким и конкретным
- НЕ начинай ответ с извинений ("извините", "к сожалению", "нам очень жаль" и т.п.) и не вставляй их в ответ. Сразу переходи к сути. Извиняйся только если это явно указано в подсказке оператора.
"""

CREATIVE_PROMPT = """\
Ты — помощник службы поддержки компании Sleeptery.
Твоя задача — давать вежливые, эмпатичные и развёрнутые ответы клиентам.

Правила:
- Отвечай на русском языке
- Используй контекст из базы знаний как основу
- Можешь дополнять ответ общими знаниями о трекерах сна и здоровье
- Будь дружелюбным, проявляй понимание проблемы клиента
- Если ситуация неоднозначная — объясни почему так бывает и предложи что попробовать
- НЕ начинай ответ с извинений ("извините", "к сожалению", "нам очень жаль", "приносим извинения" и т.п.) и не вставляй шаблонные извинения в ответ. Сразу переходи к сути и решению. Извиняйся только если это явно указано в подсказке оператора.
"""

SYSTEM_PROMPTS = {
    "strict": STRICT_PROMPT,
    "creative": CREATIVE_PROMPT,
}

LENGTH_INSTRUCTIONS = {
    "extra_short": "Ответь ОДНИМ предложением.",
    "short": "Ответь кратко, не более 3 предложений.",
    "medium": "Ответь развёрнуто, 5-7 предложений.",
    "long": "Дай подробный, детальный ответ.",
}


def build_query_prompt(
    context: str,
    question: str,
    hint: str | None = None,
    length: str | None = None,
) -> str:
    prompt = (
        f"## Контекст из базы знаний:\n{context}\n\n"
        f"## Вопрос клиента:\n{question}"
    )
    if hint:
        prompt += f"\n\n## Подсказка от оператора:\n{hint}"
    if length and length in LENGTH_INSTRUCTIONS:
        prompt += f"\n\n## Формат ответа:\n{LENGTH_INSTRUCTIONS[length]}"
    return prompt


def build_dialog_prompt(
    context: str,
    dialog: str,
    question: str | None = None,
    hint: str | None = None,
    length: str | None = None,
) -> str:
    prompt = (
        f"## Контекст из базы знаний:\n{context}\n\n"
        f"## Диалог с клиентом:\n{dialog}"
    )
    if question:
        prompt += f"\n\n## Вопрос оператора:\n{question}"
    if hint:
        prompt += f"\n\n## Подсказка от оператора:\n{hint}"
    if length and length in LENGTH_INSTRUCTIONS:
        prompt += f"\n\n## Формат ответа:\n{LENGTH_INSTRUCTIONS[length]}"
    return prompt
